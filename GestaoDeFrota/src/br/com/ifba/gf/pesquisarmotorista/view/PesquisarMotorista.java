    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        Motorista motorista = new Motorista();
        int y = 0;
        
        List <Motorista> buscaCPF = Singleton.getInstance().findByCPF(this.txtCPFMotorista.getText());
        
        for(int i = 0 ; i < buscaCPF.size() ; i++){
            y++;
            modelo.addRow(new Object[]{
                
                buscaCPF.get(i).getNomeMotorista(),
                buscaCPF.get(i).getRg(),
                buscaCPF.get(i).getCnh(),
            });
        }
        
        if(y == 0){
            JOptionPane.showMessageDialog(null, "Motorista nÃ£o cadastrado!");
        }
        
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

